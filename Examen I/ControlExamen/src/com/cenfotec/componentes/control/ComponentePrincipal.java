    private void generateRandomLetters(){
        int range = 6;
      
       for(int i = 0; i <= 3; i++ ){
           int rand = ( int )( Math.random() * range );
           System.out.println(rand);
           combination[i] = letters[rand];
       }
    }
     
    private void turnIntoArray(){
        String tried = attempt.getText();
        char[] x = new char[4];
        
        if(tried.length() != 4){
            erroMsg();
        }else{
            for( int i = 0; i < tried.length(); i++ ){
                x[i] = tried.charAt(i);
            }
            validateLetters(x);
        }
    }
    
    private void validateLetters(char[] x){
        int total = compare(x);
        
        if( total != 0 && total < 4){
            successTotalMsg(total);
            int exist = exist(x);
            
            if(exist != 0 && exist < 4){
                successPartialMsg(exist);
            }
            
            subtractAttempts();
            
        }else if(total == 4){
            
            successTotalMsg(total);
            disableBtn();
            btnStartGame.setEnabled(true);
            result.setText("Usted ganÃ³");
            result2.setText("");
            generateRandomLetters();
        }
    }
    
    private int exist(char[] x){
        int cont = 0;
         
        for(int i = 0; i < x.length; i++){ 
            for ( String letter : combination) {
                if (letter.equals( String.valueOf(x[i]) )) {
                    cont += 1;
                }   
            }
        }
        return cont;
    }
    
    private void successPartialMsg(int num){
       result2.setText("Aciertos parciales: " + num);
    }
    
    private void successTotalMsg(int num){
        result.setText("Aciertos totales: " + num);
    }
    
    private void disableBtn(){
        attempt.setEnabled(false);
        btnSubmit.setEnabled(false);
    }
    
    private void erroMsg(){
        if(attempts != 0){
            result.setText("Intentelo de nuevo");
            subtractAttempts();
        }else{
            result.setText("Game Over");
            btnStartGame.setEnabled(true);
            disableBtn();
        }
    }
    
    private void subtractAttempts(){
        if(attempts != 0){
            attempts -= 1;
            attemptsAvailable.setText(String.valueOf(attempts));
        }else{
            erroMsg();
        }
    }
    
    private int compare(char[] x){
        int cont = 0;
        
        for(int i = 0; i < combination.length; i++){
            if(combination[i].equals(String.valueOf(x[i]))){
                cont += 1 ;
            }
        }
        return cont;
    }

    
